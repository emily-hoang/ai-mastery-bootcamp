import numpy as np

array = np.array([10, 20, 30])
print("This is the item in the array with index 0, 1, 2", array[0], array[1], array[2])
print("This is the item from index 1 to 3", array[1:4])
print("This is the item from index 0 to 2", array[:2])
print("This is the item from index 2 to the last one", array[2:])
